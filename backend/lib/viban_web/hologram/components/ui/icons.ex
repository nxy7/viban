    size_class =
      case size do
        "xs" -> "w-3 h-3"
        "sm" -> "w-4 h-4"
        "md" -> "w-5 h-5"
        "lg" -> "w-6 h-6"
        "xl" -> "w-8 h-8"
        _ -> "w-5 h-5"
      end

    if extra_class == "" do
      size_class
    else
      size_class <> " " <> extra_class
    end
